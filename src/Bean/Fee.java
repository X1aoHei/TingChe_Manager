package Bean;

public class Fee {

	private String id;
	private String cheweihao;
	private String fee;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getCheweihao() {
		return cheweihao;
	}
	public void setCheweihao(String cheweihao) {
		this.cheweihao = cheweihao;
	}
	public String getFee() {
		return fee;
	}
	public void setFee(String fee) {
		this.fee = fee;
	}
}
