package Bean;

public class TingCheInfo {

	private String id;
	private String chepai;
	private String juli;
	private String in_data;
	private String out_data;
	private String cheweihao;
	private String fee;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getChepai() {
		return chepai;
	}
	public void setChepai(String chepai) {
		this.chepai = chepai;
	}
	public String getJuli() {
		return juli;
	}
	public void setJuli(String juli) {
		this.juli = juli;
	}
	public String getIn_data() {
		return in_data;
	}
	public void setIn_data(String in_data) {
		this.in_data = in_data;
	}
	public String getOut_data() {
		return out_data;
	}
	public void setOut_data(String out_data) {
		this.out_data = out_data;
	}
	public String getCheweihao() {
		return cheweihao;
	}
	public void setCheweihao(String cheweihao) {
		this.cheweihao = cheweihao;
	}
	public String getFee() {
		return fee;
	}
	public void setFee(String fee) {
		this.fee = fee;
	}
}
